// WAP to declare and initialize all the primittive type of variables and print the result on the console.
class Variable {
    public static void main(String[] arga){
        //declare variable
        // float or double
        double marks;
        int age;
        char section;
        // initialize     
        // identifier = value or literal
        marks = 40.5;
        age = 40;
        section = 'C';
        // declare and initialize an string variable
        String name = "Anushka Shrivastava";
        // printing variables values of console/output 
        System.out.println("Marks = " + marks);
        System.out.println("Age = " + age);
        System.out.println("Section = " + section);
        System.out.println("Name = " + name);
    }
}
